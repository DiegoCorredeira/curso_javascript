// Percorrendo uma lista

// Criando uma lista de frutas
frutas = ['Banana', 'Maçã', 'Pera', 'Uva', 'Melancia', 'Abacaxi', 'Laranja', 'Limão', 'Mamão', 'Manga']

// Percorrendo a lista de frutas com o for in e imprimindo no console
for (fruta in frutas){
    console.log(`${fruta} - ${frutas[fruta]}`)
}