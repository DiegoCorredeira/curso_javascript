/**
 * Ordenar um array:
 * Crie um array de palavras.
 * Ordene o array em ordem alfabética crescente.
 * Ordene o array em ordem alfabética decrescente.
 * Imprima o array em ambas as ordenações no console.
 */

// Criando um array de palavras
const palavras = ["casa", "carro", "bicicleta", "moto", "aviao"];

// Ordenando o array em ordem alfabetica crescente
palavras.sort();

// Imprimindo o array
console.log(palavras);

// Ordenando o array em ordem alfabetica decrescente
palavras.reverse();

// Imprimindo o array
console.log(palavras);
